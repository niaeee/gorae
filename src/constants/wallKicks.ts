/**
 * O 블록용 벽 킥 데이터
 * O 블록은 회전해도 모양이 같으므로 벽 킥이 필요 없습니다.
 */
export const WALL_KICK_DATA_O: Record<string, WallKickOffset[]> = {
  '0->1': [{ x: 0, y: 0 }],
  '1->0': [{ x: 0, y: 0 }],
  '1->2': [{ x: 0, y: 0 }],
  '2->1': [{ x: 0, y: 0 }],
  '2->3': [{ x: 0, y: 0 }],
  '3->2': [{ x: 0, y: 0 }],
  '3->0': [{ x: 0, y: 0 }],
  '0->3': [{ x: 0, y: 0 }],
};

/**
 * 테트로미노 타입에 따라 적절한 벽 킥 데이터를 반환하는 함수
 */
export function getWallKickData(type: string): Record<string, WallKickOffset[]> {
  if (type === 'I') {
    return WALL_KICK_DATA_I;
  } else if (type === 'O') {
    return WALL_KICK_DATA_O;
  } else {
    return WALL_KICK_DATA_JLSTZ;
  }
}
